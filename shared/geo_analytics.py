# County-level rate variations (synthetic but realistic)
# ─────────────────────────────────────────────────────

NJ_COUNTY_RATE_FACTORS: dict[str, float] = {
    "Atlantic": 1.02, "Bergen": 1.08, "Burlington": 0.98,
    "Camden": 0.97, "Cape May": 1.05, "Cumberland": 0.94,
    "Essex": 1.10, "Gloucester": 0.96, "Hudson": 1.12,
    "Hunterdon": 1.01, "Mercer": 1.03, "Middlesex": 1.00,
    "Monmouth": 1.04, "Morris": 1.06, "Ocean": 1.01,
    "Passaic": 1.07, "Salem": 0.93, "Somerset": 1.05,
    "Sussex": 0.99, "Union": 1.09, "Warren": 0.95,
}


def estimate_county_bill(base_bill: float, county: str) -> dict:
    """
    Estimate bill for a county based on rate factor.
    Returns dict with estimated bill, NJ average, and comparison.
    """
    factor = NJ_COUNTY_RATE_FACTORS.get(county, 1.0)
    estimated = round(base_bill * factor, 2)
    nj_avg = round(base_bill, 2)
    diff = round(estimated - nj_avg, 2)
    diff_pct = round((estimated - nj_avg) / nj_avg * 100, 2) if nj_avg else 0

    return {
        "county": county,
        "estimated_bill": estimated,
        "nj_average": nj_avg,
        "difference": diff,
        "difference_pct": diff_pct,
        "rate_factor": factor,
    }


def get_all_county_estimates(base_bill: float) -> pd.DataFrame:
    """Get bill estimates for all 21 NJ counties."""
    records = []
    for county, factor in NJ_COUNTY_RATE_FACTORS.items():
        est = round(base_bill * factor, 2)
        records.append({
            "County": county,
            "Rate Factor": factor,
            "Est. Bill ($)": est,
            "vs NJ Avg ($)": round(est - base_bill, 2),
            "vs NJ Avg (%)": round((est - base_bill) / base_bill * 100, 1) if base_bill else 0,
        })
    return pd.DataFrame(records).sort_values("Est. Bill ($)", ascending=False)


