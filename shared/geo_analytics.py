# Build reverse lookup
_ZIP_TO_COUNTY: dict[str, str] = {}
for county, zips in NJ_COUNTY_ZIPS.items():
    for z in zips:
        _ZIP_TO_COUNTY[z] = county


def zip_to_county(zip_code: str) -> str | None:
    """Map a NJ ZIP code to its county. Returns None if not found."""
    clean = str(zip_code).strip()[:5]
    return _ZIP_TO_COUNTY.get(clean)


