def load_nj_geojson() -> dict | None:
    """
    Load and filter the national GeoJSON to only NJ counties.
    Falls back to None if network unavailable.
    """
    try:
        import urllib.request
        with urllib.request.urlopen(NJ_GEOJSON_URL, timeout=10) as resp:
            full = json.loads(resp.read().decode())
        nj_fips = set(NJ_COUNTY_FIPS.values())
        nj_features = [f for f in full["features"] if f["properties"]["STATE"] == "34"]
        return {"type": "FeatureCollection", "features": nj_features}
    except Exception:
        return None


def build_choropleth_data(base_bill: float) -> tuple[pd.DataFrame, list[str]]:
    """
    Build DataFrame for Plotly choropleth.
    Returns (df, fips_list).
    """
    records = []
    fips_list = []
    for county, fips in NJ_COUNTY_FIPS.items():
        factor = NJ_COUNTY_RATE_FACTORS.get(county, 1.0)
        est = round(base_bill * factor, 2)
        records.append({
            "fips": fips,
            "county": county,
            "estimated_bill": est,
            "rate_factor": factor,
            "vs_avg_pct": round((factor - 1.0) * 100, 1),
        })
        fips_list.append(fips)
    return pd.DataFrame(records), fips_list
